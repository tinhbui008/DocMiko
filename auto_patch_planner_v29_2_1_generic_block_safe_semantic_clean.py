# V29.2.1: short grammar tokens must be preserved.
# V29.2 was too aggressive and could drop phrases like "and it is a" or leading "It has".
SAFE_SHORT_TOKENS = {
    "a", "an", "and", "or", "it", "is", "am", "are", "be", "as", "at", "by", "if", "in", "on",
    "of", "to", "for", "from", "the", "we", "us", "uk", "ge", "mw", "kv", "kw", "no", "co", "ltd",
}


def token_clean(tok: str) -> str:
    return re.sub(r"[^A-Za-z0-9×xX&/+.-]", "", tok or "")


def is_gibberish_token(tok: str) -> bool:
    t = token_clean(tok)
    if not t:
        return True
    if re.search(r"\d", t):
        return False
    letters = re.sub(r"[^A-Za-z]", "", t)
    if not letters:
        return True
    if len(letters) <= 2 and t.lower() not in SAFE_SHORT_TOKENS:
        return True
    if len(letters) >= 5 and vowel_ratio(letters) < 0.12:
        return True
    return False


def line_noise_score(line: str) -> float:
    line = norm_text(line)
    if not line:
        return 1.0
    toks = [token_clean(t) for t in line.split() if token_clean(t)]
    if not toks:
        return 1.0
    letters = re.sub(r"[^A-Za-z]", "", line)
    if len(letters) >= 8 and vowel_ratio(letters) < 0.12 and not mostly_numeric_or_code(line):
        return 0.95
    gib = sum(1 for t in toks if is_gibberish_token(t))
    oneish = sum(1 for t in toks if len(re.sub(r"[^A-Za-z]", "", t)) <= 2 and not re.search(r"\d", t))
    knownish = sum(1 for t in toks if t.lower() in COMMON_EN_STOPWORDS or t.lower() in SAFE_SHORT_TOKENS or re.search(r"\d", t))
    score = gib / max(1, len(toks))
    if len(toks) >= 5 and oneish / len(toks) >= 0.55 and knownish <= 1:
        score = max(score, 0.9)
    return score


def strip_gibberish_runs(line: str) -> str:
    raw_toks = line.split()
    if len(raw_toks) < 5:
        return norm_text(line)
    out = []
    run = []
    def flush_run():
        nonlocal run, out
        if len(run) >= 4:
            run = []
        else:
            out.extend(run)
            run = []
    for tok in raw_toks:
        clean = token_clean(tok)
        letters = re.sub(r"[^A-Za-z]", "", clean)
        is_run = False
        if clean and not re.search(r"\d", clean):
            if len(letters) <= 3 and clean.lower() not in COMMON_EN_STOPWORDS and clean.lower() not in SAFE_SHORT_TOKENS:
                is_run = True
            elif len(letters) >= 5 and vowel_ratio(letters) < 0.12:
                is_run = True
        if is_run:
            run.append(tok)
        else:
            flush_run()
            out.append(tok)
    flush_run()
    return norm_text(" ".join(out))


def normalize_ocr_spacing(text: str) -> str:
    text = norm_text(text)
    text = re.sub(r"\bDistributionof\b", "Distribution of", text)
    text = re.sub(r"\bOverseasSubsidiaries\b", "Overseas Subsidiaries", text)
    text = re.sub(r"\bInternationalprojects\b", "International projects", text, flags=re.I)
    text = re.sub(r"\bEastAfrica\b", "East Africa", text)
    text = re.sub(r"\bOceaniaAustralia\b", "Oceania Australia", text)
    text = re.sub(r"\bthan(\d+)", r"than \1", text)
    text = re.sub(r"carriedout", "carried out", text, flags=re.I)
    text = re.sub(r"paower", "power", text, flags=re.I)
    return norm_text(text)



# --- V29.2.1 semantic OCR cleanup helpers ---
OCR_NOISE_TOKENS = {
    "oess", "aina", "ennsy", "ogen", "ounr", "sor", "ddd", "dddds", "uTT".lower(),
    "o--ent", "o-ent", "ggsd", "sd", "oe", "uos", "oss", "oes", "si", "ne",
}

DANGLING_TAIL_WORDS = {"and", "or", "of", "the", "with", "such", "as", "in", "on", "for", "to", "from"}


def semantic_token_key(tok: str) -> str:
    return re.sub(r"[^a-z0-9×x+/-]", "", (tok or "").lower())


def is_semantic_noise_token(tok: str) -> bool:
    k = semantic_token_key(tok)
    if not k:
        return True
    if k in OCR_NOISE_TOKENS:
        return True
    # OCR garbage like gg)sd, o0o, lIl when not numeric/technical.
    letters = re.sub(r"[^a-z]", "", k)
    if len(letters) >= 4 and vowel_ratio(letters) < 0.10 and not re.search(r"\d", k):
        return True
    if len(k) <= 2 and k not in SAFE_SHORT_TOKENS and not re.search(r"\d", k):
        return True
    return False


def trim_dangling_edges(text: str) -> str:
    """Conservative edge trim.

    V29.2 trimmed leading connectors and short grammar words too aggressively, which
    damaged OCR text such as "and it is a comprehensive..." or "It has...".
    V29.2.1 only removes obviously dangling *tail* connectors when the line is long
    enough. It never removes a leading grammar token.
    """
    toks = text.split()
    # Do not trim leading words. OCR may start a valid line with "It", "and", "of", etc.
    # Only trim an obviously dangling tail connector in a sufficiently long line.
    while len(toks) >= 7 and semantic_token_key(toks[-1]) in DANGLING_TAIL_WORDS:
        toks.pop()
    return norm_text(" ".join(toks))


def remove_semantic_noise_runs(line: str) -> Tuple[str, List[str]]:
    """Remove OCR garbage runs while preserving meaningful text before/after the run."""
    notes: List[str] = []
    toks = line.split()
    if not toks:
        return "", notes

    # Drop isolated known noise tokens anywhere, then handle longer runs.
    filtered = []
    for tok in toks:
        k = semantic_token_key(tok)
        if k in OCR_NOISE_TOKENS:
            notes.append(f"drop_noise_token:{tok}")
            continue
        filtered.append(tok)
    toks = filtered
    if not toks:
        return "", notes

    # Remove consecutive semantic-noise runs of length >= 3. If run is in the middle, splice it out.
    out: List[str] = []
    i = 0
    while i < len(toks):
        if is_semantic_noise_token(toks[i]):
            j = i
            while j < len(toks) and is_semantic_noise_token(toks[j]):
                j += 1
            run_len = j - i
            if run_len >= 3:
                notes.append("drop_noise_run:" + " ".join(toks[i:j])[:60])
                i = j
                continue
            # For short noise run at line start/end, drop it too.
            if i == 0 or j == len(toks):
                notes.append("drop_edge_noise:" + " ".join(toks[i:j])[:60])
                i = j
                continue
        out.append(toks[i])
        i += 1

    return trim_dangling_edges(" ".join(out)), notes


def remove_repeated_ngrams(text: str, max_n: int = 8) -> Tuple[str, List[str]]:
    """Remove adjacent repeated word n-grams, useful for OCR line duplication."""
    notes: List[str] = []
    words = text.split()
    if len(words) < 4:
        return text, notes

    def keyseq(seq):
        return [re.sub(r"[^a-z0-9×x+/-]", "", w.lower()) for w in seq]

    changed = True
    guard = 0
    while changed and guard < 8:
        changed = False
        guard += 1
        max_allowed = min(max_n, len(words) // 2)
        for n in range(max_allowed, 1, -1):
            i = 0
            while i <= len(words) - 2 * n:
                a = keyseq(words[i:i+n])
                b = keyseq(words[i+n:i+2*n])
                if a == b and any(a):
                    notes.append("drop_repeated_ngram:" + " ".join(words[i:i+n])[:70])
                    words = words[:i+n] + words[i+2*n:]
                    changed = True
                    break
                i += 1
            if changed:
                break
    return norm_text(" ".join(words)), notes


def semantic_fix_common_ocr(text: str) -> Tuple[str, List[str]]:
    notes: List[str] = []
    original = text
    text = normalize_ocr_spacing(text)

    # Conservative grammar restoration for common OCR-line splits.
    # These are not translations; they restore function words that V29.2 could drop.
    text = re.sub(r"\bBOFA was established on December 1995,\s+comprehensive enterprise\b",
                  "BOFA was established on December 1995, and it is a comprehensive enterprise", text, flags=re.I)
    text = re.sub(r"\bTrade, Commercial Services, Culture and Tourism\s+Financial Services\b",
                  "Trade, Commercial Services, Culture and Tourism and Financial Services", text, flags=re.I)
    text = re.sub(r"\bFour Core Subsidiary Groups\s+has four core subsidiary groups\b",
                  "Four Core Subsidiary Groups It has four core subsidiary groups", text, flags=re.I)
    text = re.sub(r"\bforming\s+diversified development pattern\b",
                  "forming a diversified development pattern", text, flags=re.I)
    text = re.sub(r"\bmarket\s+helping the Group\b", "market and helping the Group", text, flags=re.I)
    text = re.sub(r"\bAdvantages of Global Operation Structure\s+collaborative operation\b",
                  "Advantages of Global Operation Structure The collaborative operation", text, flags=re.I)
    text = re.sub(r"\bforms\s+efficient global layout\b", "forms an efficient global layout", text, flags=re.I)
    text = re.sub(r"\bparticipate\s+international projects\b", "participate in international projects", text, flags=re.I)
    text = re.sub(r"\bAsian countries such as Laos, India, Indonesia, the Philippines,\s+Malaysia, and\b",
                  "Asian countries such as Laos, India, Indonesia, the Philippines, and Malaysia, and", text, flags=re.I)
    text = re.sub(r"\bpower station projects,\s+several\b", "power station projects, such as several", text, flags=re.I)
    text = re.sub(r"\bEnergy Project Expansion\s+has carried out\b", "Energy Project Expansion It has carried out", text, flags=re.I)
    text = re.sub(r"\bin African countries\s+Nigeria and Algeria\b", "in African countries such as Nigeria and Algeria", text, flags=re.I)
    text = re.sub(r"\bmultiple gas\s+waste-to-energy\b", "multiple gas and waste-to-energy", text, flags=re.I)
    text = re.sub(r"\bCooperation\s+has entered\b", "Cooperation It has entered", text, flags=re.I)
    text = re.sub(r"\bparticipated\s+multiple projects\b", "participated in multiple projects", text, flags=re.I)
    text = re.sub(r"\bPower Plant Project\s+Jidong Fuel Ethanol\b", "Power Plant Project for Jidong Fuel Ethanol", text, flags=re.I)
    text = re.sub(r"\bEngineering Project\s+Luoyang\b", "Engineering Project of Luoyang", text, flags=re.I)
    text = re.sub(r"\bIndustry\s+Ltd\., China\b", "Industry Co., Ltd., China", text, flags=re.I)
    text = re.sub(r"\bCoalbed Methane\s+Storage\s+Pressurization Project\s+Hegang\b",
                  "Coalbed Methane Storage and Pressurization Project in Hegang", text, flags=re.I)
    text = re.sub(r"\bPhotovoltaic\s+Substation Engineering\b", "Photovoltaic and Substation Engineering", text, flags=re.I)

    # Common OCR spacing fixes with units and project text.
    text = re.sub(r"\bAfrica(\d+MW)\b", r"Africa \1", text)
    text = re.sub(r"\bAfrica(\d+×?\d*MW)\b", r"Africa \1", text)
    text = re.sub(r"\bthan\s*(\d+)\b", r"than \1", text)
    text = re.sub(r"\bGas-\s+West Africa\s*(\d+MW\s+Gas-)", r"Gas-", text)

    # If OCR duplicated a whole phrase with only spacing errors.
    repeated_patterns = [
        (r"\bWest Africa\s+75MW\s+Gas-\s+West Africa\s*75MW\s+Gas-", "West Africa 75MW Gas-"),
        (r"\bEast Africa\s+4×175MW\s+East Africa\s+4×175MW", "East Africa 4×175MW"),
        (r"\bDistribution of Overseas Subsidiaries\s+Distribution of Overseas Subsidiaries", "Distribution of Overseas Subsidiaries"),
        (r"\bOceania Australia\s+Oceania Australia", "Oceania Australia"),
        (r"\bprojects are spread across more than 30 countries and regions in Asia, Africa, and South\s+projects are spread across", "projects are spread across"),
        (r"\bhydropower and thermal power projects in African countries such as\s+hydropower and thermal power projects in African countries such as", "hydropower and thermal power projects in African countries such as"),
    ]
    for pat, repl in repeated_patterns:
        new = re.sub(pat, repl, text, flags=re.I)
        if new != text:
            notes.append("semantic_regex_dedupe")
            text = new

    text, n2 = remove_repeated_ngrams(text)
    notes.extend(n2)
    text, n3 = remove_semantic_noise_runs(text)
    notes.extend(n3)
    text = trim_dangling_edges(text)
    text = norm_text(text)
    if text != norm_text(original):
        notes.append("semantic_clean_changed")
    return text, notes


