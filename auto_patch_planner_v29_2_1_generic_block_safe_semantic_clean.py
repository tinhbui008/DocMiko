def main():
    ap = argparse.ArgumentParser(description="Generic block-level OCR patch planner for arbitrary PDFs.")
    ap.add_argument("--ocr-report", required=True, help="OCR report JSON containing items with page, text, bbox, confidence.")
    ap.add_argument("--pdf", default=None, help="Original/input PDF. Used for page sizes only.")
    ap.add_argument("--output-patch-map", required=True)
    ap.add_argument("--output-image-region-map", required=True)
    ap.add_argument("--report-json", required=True)
    ap.add_argument("--translation-mode", default="source", choices=["source", "dictionary", "builtin", "map_or_builtin", "blank"], help="source=layout test; map_or_builtin uses --translation-map then built-in dictionary.")
    ap.add_argument("--translation-map", default=None, help="Optional CSV/JSON map: source -> translation.")
    ap.add_argument("--brand-keep-file", default=None, help="Optional txt file with one brand/protected term per line.")
    ap.add_argument("--min-confidence", type=float, default=0.35)
    ap.add_argument("--min-chars", type=int, default=2)
    ap.add_argument("--protect-top-band", type=float, default=95.0, help="Preserve top header/logo band in PDF points. Use 0 to disable.")
    ap.add_argument("--allow-cjk", action="store_true", help="Do not reject CJK-only OCR items. Default rejects/preserves them.")
    ap.add_argument("--no-preserve-brands", action="store_true", help="Allow brand terms to be patched. Default preserves brands/logos.")
    ap.add_argument("--block-gap-factor", type=float, default=1.10)
    ap.add_argument("--min-x-overlap", type=float, default=0.18)
    ap.add_argument("--max-lines-per-block", type=int, default=8)
    ap.add_argument("--fill", default="sample", choices=["sample", "white", "none"], help="Mask/fill mode passed to renderer.")
    ap.add_argument("--drop-missing-translations", action="store_true", help="When using translation modes other than source, skip blocks with no translation instead of keeping source.")
    args = ap.parse_args()

    build_patch_and_regions(
        ocr_report=args.ocr_report,
        pdf=args.pdf,
        output_patch_map=args.output_patch_map,
        output_image_region_map=args.output_image_region_map,
        report_json=args.report_json,
        translation_mode=args.translation_mode,
        translation_map_path=args.translation_map,
        brand_keep_file=args.brand_keep_file,
        min_confidence=args.min_confidence,
        min_chars=args.min_chars,
        protect_top_band=args.protect_top_band,
        reject_cjk=not args.allow_cjk,
        preserve_brands=not args.no_preserve_brands,
        block_gap_factor=args.block_gap_factor,
        min_x_overlap=args.min_x_overlap,
        max_lines_per_block=args.max_lines_per_block,
        fill_mode=args.fill,
        keep_untranslated=not args.drop_missing_translations,
    )


if __name__ == "__main__":
    main()
